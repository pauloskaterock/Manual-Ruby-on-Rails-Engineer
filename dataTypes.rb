# Em Ruby, os tipos de dados (datatypes) referem-se aos diferentes 
# tipos de valores que podem ser armazenados e manipulados em um programa. 
# Aqui estão alguns dos principais tipos de dados em Ruby:

# 1. Integer:
# Representa números inteiros, como 1, -5, 100.

# Exemplo:

numero_inteiro = 42

# 2. Float:
# Representa números decimais (ponto flutuante), como 3.14, -0.5.

# Exemplo:

numero_decimal = 3.14

# 3. String:
# Representa sequências de caracteres, como "Olá, Ruby!".

# Exemplo:

mensagem = "Olá, Ruby!"

# 4. Boolean:
# Representa valores lógicos verdadeiro (true) ou falso (false).

# Exemplo:

status = true

# 5. Array:
# Representa uma coleção ordenada de valores, acessíveis por um índice.

# Exemplo:

lista_numeros = [1, 2, 3, 4, 5]

# 6. Hash:
# Representa uma coleção de pares chave-valor, onde cada chave é única.

# Exemplo:

informacoes_pessoa = { "nome" => "João", "idade" => 25, "cidade" => "São Paulo" }

# 7. Symbol:
# Representa identificadores imutáveis, frequentemente usados como chaves em hashes.

# Exemplo:

estado = :sp

# 8. Nil:
# Representa a ausência de valor. Em Ruby, é indicado pelo nil.

# Exemplo:

valor_nulo = nil

# 9. Range:
# Representa um intervalo de valores.

# Exemplo:

intervalo = (1..5)

# 10. Regexp (Expressões Regulares):
# Representa padrões de texto para pesquisa e manipulação.

# Exemplo:

expressao_regular = /\d{3}-\d{4}/

# 11. Class:
# Representa tipos de classes em Ruby.

# Exemplo:

classe_pessoa = Pessoa.new