# Faça um programa que leia um número e exiba o seu quadrado.


#para calcular a raiz quadrada temos o metodo Math

number = 16
raiz_quadrada = Math.sqrt(number)

puts "A raiz quadrada de #{number} é #{raiz_quadrada}"


# Em Ruby, você pode calcular a raiz quadrada de um número utilizando o método Math.sqrt(). Este método retorna a raiz quadrada do número passado como argumento. Aqui está um exemplo de como usar esse método:

#     
#     # Calculando a raiz quadrada de um número
#     numero = 16
#     raiz_quadrada = Math.sqrt(numero)
#     puts "A raiz quadrada de #{numero} é #{raiz_quadrada}"
#     Neste exemplo, o valor de numero é 16. A função Math.sqrt() é utilizada para calcular a raiz quadrada desse número, e o resultado é armazenado na variável raiz_quadrada. Em seguida, usamos puts para exibir o resultado na tela.
    
#     Lembre-se de que o método Math.sqrt() retorna um número em ponto flutuante (float), mesmo se o número original for um inteiro.